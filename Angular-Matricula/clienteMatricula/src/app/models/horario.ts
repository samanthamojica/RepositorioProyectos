export class Horario {
  public statusDia: boolean;
  public dia: string;
  public hora: string;
  public materia: string;
  public idClase: string;

  constructor() {
    this.statusDia = this.statusDia;
    this.dia = this.dia;
    this.hora = this.hora;
    this.materia = this.materia;
    this.idClase = this.idClase;
  }
}
